// Switch case

void main() {
  String str = "Kanha";
  switch(str) {
    case "Ashish" :
      print("Barclays");
      break;
    case "Rahul" :
      print("BMC Software");
      break;
    case "Badhe" :
      print("CarPro Systems");
      break;
    case "Kanha" :
      print("Infosys");
      break;
    default:
      print("Wrong Input");
      break;
  }
}