void main() {
  dynamic x = "kanha";
  dynamic y = 10.5;
  dynamic z = 20;
  dynamic a = false;

  print(x.runtimeType);
  print(y.runtimeType);
  print(z.runtimeType);
  print(a.runtimeType);

  x = 2.5; 
  // we can store and assign value of any datatype and can be changed the datatype at runtime

}